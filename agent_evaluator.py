def train_model(

    model,

    train_loader,

    num_epochs=5
):

    optimizer = torch.optim.AdamW(

        model.parameters(),

        lr=2e-5
    )

    criterion = nn.MSELoss()

    device = torch.device(

        "cuda"

        if torch.cuda.is_available()

        else "cpu"
    )

    model.to(device)

    losses = []

    for epoch in range(num_epochs):

        model.train()

        total_loss = 0

        for batch in train_loader:

            optimizer.zero_grad()

            input_ids = batch[
                "input_ids"
            ].to(device)

            attention_mask = batch[
                "attention_mask"
            ].to(device)

            labels = batch[
                "labels"
            ].to(device)

            outputs = model(

                input_ids,

                attention_mask
            )

            loss = criterion(

                outputs,

                labels
            )

            loss.backward()

            optimizer.step()

            total_loss += loss.item()

        avg_loss = (
            total_loss /
            len(train_loader)
        )

        losses.append(avg_loss)

        print(

            f"Epoch {epoch+1} "
            f"Loss: {avg_loss:.4f}"
        )

    return losses

