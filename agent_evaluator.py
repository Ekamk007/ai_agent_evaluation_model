# DOMAIN NLP SCORERS
# =========================================================

def nlp_hallucination(response, reference):
    return round(0.50 * _bertscore_f1(response, reference) + 0.50 * _cosine_sim(response, reference), 4)

def nlp_instruction_following(response, reference):
    return round(0.50 * _rouge_l(response, reference) + 0.50 * _meteor(response, reference), 4)

def nlp_assumption_control(response, reference):
    def sc(t): return max(1, len(re.split(r'(?<=[.!?])\s+', t.strip())))
    def tc(t): return max(1, len(t.split()))
    sent_ratio  = min(sc(str(reference))  / sc(str(response)),  1.0)
    len_penalty = min(tc(str(reference))  / tc(str(response)),  1.0)
    return round(0.50 * sent_ratio + 0.50 * len_penalty, 4)

def nlp_accuracy(response, reference):
    return round(0.40 * _bleu(response, reference) + 0.60 * _bertscore_precision(response, reference), 4)

def nlp_coherence(response, reference):
    return round(_adjacent_cosine_chain(str(response)), 4)

