JUDGE_SYSTEM_PROMPT = """
You are an expert NLP evaluator.

Score the MODEL RESPONSE on exactly 5 dimensions.

1. hallucination (0–10)
   0 = heavily fabricated / unsupported facts
   10 = fully grounded in the reference, no invented content

2. instruction_following (0–10)
   0 = ignores the prompt entirely
   10 = addresses every instruction completely and precisely

3. assumption_control (0–10)
   0 = invents many details not present in the prompt / reference
   10 = introduces no unjustified assumptions

4. accuracy (0–10)
   0 = factually wrong relative to the reference
   10 = completely factually correct and precise

5. coherence (0–10)
   0 = incoherent, contradictory, or disjointed
   10 = logically structured, fluent, well-organised

Return ONLY valid JSON — no preamble, no explanation, no markdown.

{
  "hallucination": <int 0-10>,
  "instruction_following": <int 0-10>,
  "assumption_control": <int 0-10>,
  "accuracy": <int 0-10>,
  "coherence": <int 0-10>
}
"""

# =========================================================
# LLM JUDGE CALL
# =========================================================

def llm_judge(prompt, response, reference, judge_model):
    user_msg = f"""PROMPT:\n{prompt}\n\nREFERENCE ANSWER:\n{reference}\n\nMODEL RESPONSE:\n{response}\n\nScore the MODEL RESPONSE using the 5 dimensions described."""

    completion = client.chat.completions.create(
        model=judge_model,
        temperature=0,
        messages=[
            {"role": "system", "content": JUDGE_SYSTEM_PROMPT},
            {"role": "user",   "content": user_msg},
        ],
    )

    resp_content = completion.choices[0].message.content.strip()

    try:
        raw = json.loads(resp_content)
    except json.JSONDecodeError:
        start = resp_content.find("{")
        end   = resp_content.rfind("}") + 1
        raw   = json.loads(resp_content[start:end])

    return {k: round(v / 10.0, 4) for k, v in raw.items()}

# =========================================================
# DOMAIN CONFIG
# =========================================================

NLP_WEIGHT   = 0.40
JUDGE_WEIGHT = 0.60

DOMAINS = ["hallucination", "instruction_following", "assumption_control", "accuracy", "coherence"]

NLP_FNS = {
    "hallucination":         nlp_hallucination,
    "instruction_following": nlp_instruction_following,
    "assumption_control":    nlp_assumption_control,
    "accuracy":              nlp_accuracy,
    "coherence":             nlp_coherence,
}

# =========================================================
# EVALUATE ONE ROW
# =========================================================

def evaluate_row(prompt, response, reference, judge_model):
    nlp_scores = {}
    for domain, fn in NLP_FNS.items():
        try:
            nlp_scores[domain] = fn(response, reference)
        except Exception as e:
            print(f"  [NLP/{domain}] error: {e}")
            nlp_scores[domain] = 0.0

    try:
        judge_scores = llm_judge(prompt, response, reference, judge_model)
        print(f"  [Judge: {judge_model.split('/')[1]}]", end=" ")
    except Exception as e:
        print(f"  [LLM Judge] error: {e}")
        judge_scores = {d: 0.0 for d in DOMAINS}

    result = {}
    domain_final = []
    for domain in DOMAINS:
        nlp_val   = nlp_scores.get(domain, 0.0)
        judge_val = judge_scores.get(domain, 0.0)
        blended   = round(NLP_WEIGHT * nlp_val + JUDGE_WEIGHT * judge_val, 4)
        result[f"{domain}_nlp"]   = nlp_val
        result[f"{domain}_llm"]   = judge_val
        result[f"{domain}_score"] = blended
        domain_final.append(blended)

    result["final_score"] = round(float(np.mean(domain_final)), 4)
    return result

