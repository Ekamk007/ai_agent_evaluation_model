def _cosine_sim(a, b):
    emb = embedding_model.encode([str(a), str(b)], convert_to_tensor=True)
    return float(util.cos_sim(emb[0], emb[1]).item())

def _rouge_l(response, reference):
    return rouge.score(str(reference), str(response))["rougeL"].fmeasure

def _bleu(response, reference):
    return sacrebleu.sentence_bleu(str(response), [str(reference)]).score / 100.0

def _meteor(response, reference):
    return meteor_score([str(reference).split()], str(response).split())

def _bertscore_f1(response, reference):
    _, _, F1 = bertscore([str(response)], [str(reference)], lang="en", verbose=False)
    return float(F1[0])

def _bertscore_precision(response, reference):
    P, _, _ = bertscore([str(response)], [str(reference)], lang="en", verbose=False)
    return float(P[0])

def _adjacent_cosine_chain(text):
    sentences = [s.strip() for s in re.split(r'(?<=[.!?])\s+', str(text)) if len(s.strip()) > 5]
    if len(sentences) < 2:
        return 0.8
    embs = embedding_model.encode(sentences, convert_to_tensor=True)
    sims = [float(util.cos_sim(embs[i], embs[i+1]).item()) for i in range(len(embs)-1)]
    return float(np.mean(sims))

