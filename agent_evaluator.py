class AgenticEvalDataset(Dataset):

    def __init__(

        self,
        dataframe,
        tokenizer,
        max_length=256,
        scoring_cols=None
    ):

        self.df = dataframe
        self.tokenizer = tokenizer
        self.max_length = max_length
        self.scoring_cols = scoring_cols

    def __len__(self):

        return len(self.df)

    def __getitem__(self, idx):

        row = self.df.iloc[idx]

        text = row["bert_input"]

        enc = self.tokenizer(

            text,

            truncation=True,

            padding="max_length",

            max_length=self.max_length,

            return_tensors="pt"
        )

        item = {

            "input_ids":
                enc["input_ids"].squeeze(0),

            "attention_mask":
                enc["attention_mask"].squeeze(0)
        }

        labels = row[
            self.scoring_cols
        ].values.astype(float)

        item["labels"] = torch.tensor(
            labels,
            dtype=torch.float
        )

        return item

