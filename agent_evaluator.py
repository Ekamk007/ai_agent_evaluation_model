def evaluate_model(

    model,

    test_loader
):

    device = torch.device(

        "cuda"

        if torch.cuda.is_available()

        else "cpu"
    )

    model.eval()

    preds = []

    actuals = []

    with torch.no_grad():

        for batch in test_loader:

            input_ids = batch[
                "input_ids"
            ].to(device)

            attention_mask = batch[
                "attention_mask"
            ].to(device)

            labels = batch[
                "labels"
            ]

            outputs = model(

                input_ids,

                attention_mask
            )

            # SAFE CONVERSION
            outputs = outputs.detach().cpu().tolist()

            labels = labels.detach().cpu().tolist()

            preds.extend(outputs)

            actuals.extend(labels)

    preds = np.array(preds)

    actuals = np.array(actuals)

    mse = mean_squared_error(

        actuals,

        preds
    )

    rmse = np.sqrt(mse)

    mae = mean_absolute_error(

        actuals,

        preds
    )

    r2 = r2_score(

        actuals,

        preds
    )

    print("\nFINAL METRICS\n")

    print(f"MSE  : {mse:.4f}")

    print(f"RMSE : {rmse:.4f}")

    print(f"MAE  : {mae:.4f}")

    return preds, actuals

